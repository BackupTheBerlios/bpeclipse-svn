package org.bpeclipse.plugin;

public class BPPluginException extends Exception {

    public BPPluginException() {
        super();
    }

    public BPPluginException(String message, Throwable cause) {
        super(message, cause);
    }

    public BPPluginException(String message) {
        super(message);
    }

    public BPPluginException(Throwable cause) {
        super(cause);
    }

}
